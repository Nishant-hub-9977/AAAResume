// Theme configuration
export type ThemeMode = 'light' | 'dark' | 'system';

export const getThemePreference = (): ThemeMode => {
  if (typeof window === 'undefined') return 'system';
  const savedTheme = localStorage.getItem('theme') as ThemeMode | null;
  return savedTheme || 'system';
};

export const setThemePreference = (theme: ThemeMode): void => {
  if (typeof window === 'undefined') return;
  localStorage.setItem('theme', theme);
  applyTheme(theme);
};

export const applyTheme = (theme: ThemeMode): void => {
  if (typeof window === 'undefined') return;
  
  const isDark = 
    theme === 'dark' || 
    (theme === 'system' && window.matchMedia('(prefers-color-scheme: dark)').matches);
  
  if (isDark) {
    document.documentElement.classList.add('dark');
  } else {
    document.documentElement.classList.remove('dark');
  }
};

// Utility functions for consistent styling
export const getContrastColor = (backgroundColor: string): string => {
  // Simple contrast calculation - in production, use a more sophisticated algorithm
  const lightColors = ['50', '100', '200', '300', '400'];
  const shade = backgroundColor.split('-').pop();
  return lightColors.includes(shade || '') ? 'text-gray-900' : 'text-white';
};

export const getFocusRingClasses = (color: string = 'primary'): string => {
  return `focus:outline-none focus:ring-2 focus:ring-${color}-500 focus:ring-offset-2 dark:focus:ring-offset-gray-800`;
};

